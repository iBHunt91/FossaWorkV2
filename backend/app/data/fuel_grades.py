# Create a set for fast lookup
FUEL_GRADES_SET = set(FUEL_GRADES)

def get_ordered_fuel_grades(grade_list):
    """
    Given a list of fuel grades, return them in the order defined by FUEL_GRADES
    """
    # Convert to list if string
    if isinstance(grade_list, str):
        grade_list = [g.strip() for g in grade_list.split(',')]
    
    # Sort by position in FUEL_GRADES
    ordered = []
    for grade in FUEL_GRADES:
        if grade in grade_list:
            ordered.append(grade)
    
    # Add any grades not in our list at the end
    for grade in grade_list:
        if grade not in ordered:
            ordered.append(grade)
    
    return ordered